$(document).ready(function(){
$('.row').hover( 
  function() {
    $(this)
      .animate({'background-color': '#428bca'}, 400);
      $('h1').animate({'color': '#e1ffd2'},400);
      $('p').animate({'color': 'white'},400);
      $('a').animate({'color': '#e7fd29'},400);
  },
  function() {
    $(this)
      .animate({'background-color': 'white'}, 400);
      $('h1').animate({'color': 'black'},400);
      $('p').animate({'color': 'black'},400);
      $('a').animate({'color': 'black'},400);
  });
});
